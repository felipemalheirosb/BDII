package br.unifacisa.helloworld;

public class Pessoa {
	
	private String _nome;
	private String _cpf;
	private int _idade;
	
	public String getNome() {
		return _nome;
	}
	public void setNome(String _nome) {
		this._nome = _nome;
	}
	public String getCpf() {
		return _cpf;
	}
	public void setCpf(String _cpf) {
		this._cpf = _cpf;
	}
	public int getIdade() {
		return _idade;
	}
	public void setIdade(int _idade) {
		this._idade = _idade;
	}
	
}
